#import "UIFont+ZQ.h"

@implementation UIFont (ZQ)

+ (UIFont *)defaultFontWithSize:(CGFloat)fontSize
{
//    HYd4gj HYd1gj
    UIFont *font = [UIFont fontWithName:@"HYd4gj" size:fontSize];
    
    return font;
}

@end
